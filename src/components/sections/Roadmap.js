const Roadmap = () => {
  const revealRefs = useRef([]);
  revealRefs.current = [];
  gsap.registerPlugin(ScrollTrigger);

  const addToRefs = (el) => {
    if (el && !revealRefs.current.includes(el)) {
      revealRefs.current.push(el);
    }
  };

  useLayoutEffect(() => {
    let t1 = gsap.timeline();
    revealRefs.current.forEach((el, index) => {
      t1.fromTo(
        el.childNodes[0],
        {
          y: "0",
        },
        {
          y: "-30%",

          scrollTrigger: {
            id: `section-${index + 1}`,
            trigger: el,
            start: "top center+=200px",
            end: "bottom center",
            scrub: true,
            // markers:true,
          },
        }
      );
    });

    return () => {
      if (t1) t1.kill();
    };
  }, []);

  return (
    <Section id="roadmap">
      <Title>Roadmap</Title>
      <Container>
        <SvgContainer>
          <DrawSvg />
        </SvgContainer>
        <Items>
          <Item>&nbsp;</Item>
          <RoadMapItem
            addToRef={addToRefs}
            title="Phase one Launch"
            subtext="In this phase, we will launch the Level one and Level two NFTs for each character. We will also launch the members area on our website for NFT holders to connect their wallets and access early gameplay, merchandise, and information on NFT utility."
          />
          <RoadMapItem
            addToRef={addToRefs}
            title="Smart Contract Deployment:"
            subtext="Once the smart contract is deployed, we will airdrop additional NFTs to the Level one and Level two NFT holders, based on the NFTs they hold. We will also launch the Level three and Level four NFTs along with the smart contract. This will ensure that the early adopters of the game are rewarded for their support and incentivized to continue promoting the game"
          />
          <RoadMapItem
            addToRef={addToRefs}
            title="Phase two Launch"
            subtext="In this phase, we will launch Zaliban on more platforms such as VR and AR. We will also distribute tokens as airdrops to all NFT holders, which can be resold and further distributed to others for experiencing Zaliban in the metaverse. The distribution of these tokens would be based on the level of NFT a person holds, i.e., the higher the level of NFT you have, the more tokens you get to distribute."
          />
          <RoadMapItem
            addToRef={addToRefs}
            title="New Genre Game Development"
            subtext=" In this step, we plan to develop a new genre of game using the same revenue-sharing model with NFT holders as Zaliban. This game would have different gameplay mechanics and story but would also use blockchain technology to enhance the in-game economy. This would allow us to expand our user base while keeping our existing NFT holders engaged and incentivized."
          />
          <RoadMapItem
            addToRef={addToRefs}
            title="Royalty Sharing"
            subtext="We will share a percentage of the game's revenue with NFT holders in the form of royalties. This will align our interests with the interests of NFT holders, providing them with an incentive to support and promote the game. We will continue to improve the game and provide educational resources to grow the Web3 ecosystem and qualify for Web3 Community and Education grants."
          />
        </Items>
      </Container>
    </Section>
  );
};

export default Roadmap;
